// Write a program to check whether two numbers (entered by user) are equal or not.
#include <iostream>
using namespace std;

int main() {
    // Declare the variable
    int x,y;
    cin>> x;
    cin >> y;
    // Write equality check condition
    if (x==y)
    {
        cout<<"1"<<endl;
    }else{
        cout<<"0"<<endl;
    }
    
    return 0;
}


























